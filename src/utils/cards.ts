type TBuildDeckArgs = {
    includeDating: boolean;
};

export type TDeck = {
    level1Deck: string[];
    level2Deck: string[];
    level3Deck: string[];
};

export const buildDeck = ({ includeDating }: TBuildDeckArgs): TDeck => {
    const level1Deck = [
        ...BASE_GAME_CARDS_LEVEL_1,
        ...DATING_EXPANSION_LEVEL_1,
    ];
    const level2Deck = [
        ...BASE_GAME_CARDS_LEVEL_2,
        ...DATING_EXPANSION_LEVEL_2,
    ];
    const level3Deck = [...BASE_GAME_CARDS_LEVEL_3];

    if (includeDating) {
        level1Deck.push(...DATING_EXPANSION_LEVEL_1);
        level2Deck.push(...DATING_EXPANSION_LEVEL_2);
        level3Deck.push(...DATING_EXPANSION_LEVEL_3);
    }

    return {
        level1Deck,
        level2Deck,
        level3Deck,
    };
};
